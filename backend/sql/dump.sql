  `email` varchar(255) NOT NULL COMMENT 'User email (used for login)',
  `password_hash` varchar(255) NOT NULL COMMENT 'Hashed password for authentication',
  `name` varchar(100) DEFAULT NULL COMMENT 'Display name or full name',
  `created_at` datetime DEFAULT current_timestamp() COMMENT 'Account creation date',
  `last_login` datetime DEFAULT NULL COMMENT 'Timestamp of the last login session',
  `is_active` tinyint(1) DEFAULT 1 COMMENT 'Account status (active or not)',
  PRIMARY KEY (`id`),
  UNIQUE KEY `email` (`email`)
) ENGINE=InnoDB AUTO_INCREMENT=2 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

